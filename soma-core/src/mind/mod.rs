//! SOMA Mind — neural inference engine.
//!
//! The MindEngine trait defines the abstract interface.
//! OnnxMindEngine implements it for server/desktop targets.
//! EmbeddedMindEngine (future) will implement it for ESP32/MCU.

pub mod adaptation;
pub mod onnx_engine;
pub mod tokenizer;
pub mod lora;

use anyhow::Result;

/// Configuration for MindEngine initialization (Section 4.2).
#[derive(Debug, Clone)]
pub struct MindConfig {
    pub max_program_steps: usize,
    pub temperature: f32,
    pub max_inference_time_secs: u64,
}

